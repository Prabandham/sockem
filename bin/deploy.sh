#!/usr/bin/env bash
# Reset
NC='\033[0m'       # Text Reset

# Regular Colors
Black='\033[0;30m'        # Black
Red='\033[0;31m'          # Red
Green='\033[0;32m'        # Green
Yellow='\033[0;33m'       # Yellow
Blue='\033[0;34m'         # Blue
Purple='\033[0;35m'       # Purple
Cyan='\033[0;36m'         # Cyan
White='\033[0;37m'        # White

# Bold
BBlack='\033[1;30m'       # Black
BRed='\033[1;31m'         # Red
BGreen='\033[1;32m'       # Green
BYellow='\033[1;33m'      # Yellow
BBlue='\033[1;34m'        # Blue
BPurple='\033[1;35m'      # Purple
BCyan='\033[1;36m'        # Cyan
BWhite='\033[1;37m'       # White

# Underline
UBlack='\033[4;30m'       # Black
URed='\033[4;31m'         # Red
UGreen='\033[4;32m'       # Green
UYellow='\033[4;33m'      # Yellow
UBlue='\033[4;34m'        # Blue
UPurple='\033[4;35m'      # Purple
UCyan='\033[4;36m'        # Cyan
UWhite='\033[4;37m'       # White

# TODO give option to pass in a revision manually as well.
# This will allow to reset if some commit broke on production
echo ""
echo ""
echo -e "${BGreen}Deploy started ....${NC}"
echo -e "${BRed}__________________________${NC}"
echo -e "${BCyan}Deploying reversion - ${NC} `git log -1 --pretty=format:"%h"`"

ssh deployer@51.15.245.243

cd apps/sockem

git fetch && git reset --hard origin/master

bundle

rake db:migrate

bundle exec rake assets:precompile

bundle exec rails s

